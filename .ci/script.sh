#!/bin/sh
set -e

cd $TRAVIS_BUILD_DIR
mkdir build && cd build
cmake -G"${TRAVIS_CMAKE_GENERATOR}" -DROBOTOLOGY_USES_GAZEBO:BOOL=OFF -DROBOTOLOGY_USES_GZ:BOOL=OFF -DROBOTOLOGY_ENABLE_DYNAMICS:BOOL=${ROBOTOLOGY_ENABLE_DYNAMICS} -DCMAKE_BUILD_TYPE=${TRAVIS_BUILD_TYPE} -DNON_INTERACTIVE_BUILD:BOOL=TRUE ..
cmake --build . --config $TRAVIS_BUILD_TYPE
